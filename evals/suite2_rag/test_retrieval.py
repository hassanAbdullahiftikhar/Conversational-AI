from utils.metrics import precision_at_k, recall_at_k, mean_reciprocal_rank

REPORTS = Path(__file__).parent.parent / "reports"
DATA_PATH = Path(__file__).parent.parent / "data" / "rag" / "queries.json"

with open(DATA_PATH) as f:
    RAG_QUERIES = json.load(f)

# Module-level result collector (populated by test_hit_rate_at_k)
_retrieval_results: list[dict] = []


