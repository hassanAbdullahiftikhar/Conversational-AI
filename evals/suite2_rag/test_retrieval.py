@pytest.mark.suite2
@pytest.mark.asyncio
@pytest.mark.parametrize("query", RAG_QUERIES)
async def test_hit_rate_at_k(api_client, query):
    """Tests Hit Rate (Recall@5) for individual RAG queries."""
    result = await api_client.rag_retrieve(query["query_text"], top_k=5)

    if result.get("error"):
        pytest.fail(
            f"Retrieval error for {query['query_id']}: {result.get('detail')}"
        )

    retrieved_chunks: list[dict] = result.get("chunks", [])
    retrieved_ids: list[str] = [c["chunk_id"] for c in retrieved_chunks]
    relevant_ids: list[str] = query["relevant_chunk_ids"]

    if not retrieved_ids:
        print(f"\n[WARN] {query['query_id']}: no chunks returned. Full: {result}")

    p5 = precision_at_k(retrieved_ids, relevant_ids, 5)
    r5 = recall_at_k(retrieved_ids, relevant_ids, 5)

    _retrieval_results.append(
        {
            "id": query["query_id"],
            "difficulty": query["difficulty"],
            "type": query["type"],
            "p5": p5,
            "r5": r5,
            "retrieved_ids": retrieved_ids,
            "relevant_ids": relevant_ids,
        }
    )

    assert r5 >= 0.5, (
        f"Recall@5 ({r5:.2f}) for {query['query_id']} is below threshold 0.5.\n"
        f"  Query   : {query['query_text']}\n"
        f"  Expected: {relevant_ids}\n"
        f"  Got     : {retrieved_ids[:5]}"
    )


@pytest.mark.suite2
@pytest.mark.asyncio
async def test_mrr(api_client, rag_queries):
    """Tests Mean Reciprocal Rank (MRR) across all 25 queries."""
    tasks = [api_client.rag_retrieve(q["query_text"], top_k=10) for q in rag_queries]
    responses = await asyncio.gather(*tasks)

    all_pairs: list[tuple[list[str], list[str]]] = []
    for i, res in enumerate(responses):
        if res.get("error"):
            continue
        retrieved_ids = [c["chunk_id"] for c in res.get("chunks", [])]
        relevant_ids = rag_queries[i]["relevant_chunk_ids"]
        if not retrieved_ids:
            print(f"[WARN] MRR query {i} ({rag_queries[i]['query_id']}): no chunks.")
        all_pairs.append((retrieved_ids, relevant_ids))

    if not all_pairs:
        pytest.skip("No retrieval results collected for MRR.")

    mrr = mean_reciprocal_rank(all_pairs)
    print(f"\nMRR (top-10): {mrr:.4f}")
    assert mrr >= 0.50, f"Mean Reciprocal Rank {mrr:.4f} is below threshold 0.50."


@pytest.mark.suite2
def test_retrieval_by_difficulty():
    """Validates that recall meets thresholds for each difficulty level."""
    if not _retrieval_results:
        pytest.skip("No retrieval results collected.")

    thresholds = {"easy": 0.7, "medium": 0.5, "hard": 0.3}

    print("\n" + "-" * 40)
    print("RETRIEVAL BY DIFFICULTY")
    print("-" * 40)

    for diff, threshold in thresholds.items():
        samples = [r["r5"] for r in _retrieval_results if r["difficulty"] == diff]
        if not samples:
            continue
        mean_recall = sum(samples) / len(samples)
        print(f"{diff.capitalize()}: Mean Recall@5 = {mean_recall:.2f} (Target: ≥{threshold})")
        assert mean_recall >= threshold, (
            f"{diff} difficulty recall {mean_recall:.2f} < {threshold}"
        )


@pytest.mark.suite2
def test_retrieval_by_type():
    """Diagnostic: logs precision breakdown by query type."""
    if not _retrieval_results:
        pytest.skip("No retrieval results collected.")

    print("\n" + "-" * 40)
    print("RETRIEVAL BY TYPE (Diagnostic)")
    print("-" * 40)

    for qtype in sorted({r["type"] for r in _retrieval_results}):
        samples = [r["p5"] for r in _retrieval_results if r["type"] == qtype]
        mean_p5 = sum(samples) / len(samples)
        print(f"{qtype.capitalize()}: Mean Precision@5 = {mean_p5:.2f}")


